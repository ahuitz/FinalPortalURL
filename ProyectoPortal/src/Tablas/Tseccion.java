package Tablas;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author Cliente
 */
@Entity
@Table(name = "tseccion")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Tseccion.findAll", query = "SELECT t FROM Tseccion t"),
    @NamedQuery(name = "Tseccion.findById", query = "SELECT t FROM Tseccion t WHERE t.id = :id"),
    @NamedQuery(name = "Tseccion.findByDescripcion", query = "SELECT t FROM Tseccion t WHERE t.descripcion = :descripcion")})
public class Tseccion implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Basic(optional = false)
    @Column(name = "Descripcion")
    private String descripcion;

    public Tseccion() {
    }

    public Tseccion(Integer id) {
        this.id = id;
    }

    public Tseccion(Integer id, String descripcion) {
        this.id = id;
        this.descripcion = descripcion;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Tseccion)) {
            return false;
        }
        Tseccion other = (Tseccion) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Tablas.Tseccion[ id=" + id + " ]";
    }
    
}
