    public void tablaNotaSeleccion(JTable tablaNot){
        if (tablaNot.getSelectedRow() != -1) {
            int fila = TablaNotas.getSelectedRow();
            
            
            
            String calificacion = TablaNotas.getValueAt(fila, 8).toString();
            entId = Integer.parseInt(TablaNotas.getValueAt(fila, 9).toString());
            String titulo = TablaNotas.getValueAt(fila, 1).toString();
            this.jTextFieldTitulo.setText(titulo);
            this.jTextFieldCalificacion.setText(calificacion);
            this.jTextFieldCalificacion.setEnabled(Boolean.TRUE);
            this.jButtonCalificacion.setEnabled(Boolean.TRUE);
        }
    }
