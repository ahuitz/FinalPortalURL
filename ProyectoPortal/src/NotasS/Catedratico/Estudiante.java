    private void cargarDatos() {
        
        DefaultTableModel miTablaNotas = (DefaultTableModel) TablaNotas.getModel();
        int cont = 0;
        Iterator<ListaSecCurso_EstuSec_EntAct> iteradorLisSecCur_EstuSec_EntAct = miLista.iterator();
        while (iteradorLisSecCur_EstuSec_EntAct.hasNext()) { 
            ListaSecCurso_EstuSec_EntAct ListaSecCur_EstSec_EntAct = iteradorLisSecCur_EstuSec_EntAct.next();
            
            if (ListaSecCur_EstSec_EntAct.getUsuarioEstId() == usuarioEstId) {
                if (cont == 0) {
                    this.jTextFieldApellido.setText(ListaSecCur_EstSec_EntAct.getApellidoEst());
                    this.jTextFieldCarne.setText(carneEst);
                    this.jTextFieldCurso.setText(ListaSecCur_EstSec_EntAct.getCurso());
                    this.jTextFieldNombre.setText(ListaSecCur_EstSec_EntAct.getNombreEst());
                }
                
                if (ListaSecCur_EstSec_EntAct.getEntregaRealizada() == Boolean.TRUE) {
                    Date mid = ListaSecCur_EstSec_EntAct.getActividadFechaEntrega();
                    DateFormat miFe = DateFormat.getDateInstance();
                    String miFec = miFe.format(mid);
                    
                    Date mid2 = ListaSecCur_EstSec_EntAct.getActividadFechaPublicacion();
                    DateFormat miFe2 = DateFormat.getDateInstance();
                    String miFec2 = miFe2.format(mid2);
                    
                    Object [] datosFila = {ListaSecCur_EstSec_EntAct.getTipoActividadDescripcion(), ListaSecCur_EstSec_EntAct.getActividadTitulo(), miFec, miFec2, ListaSecCur_EstSec_EntAct.getActividadPunteo(), ListaSecCur_EstSec_EntAct.getActividadFisica(), ListaSecCur_EstSec_EntAct.getActividadVirtual(), "Realizada", ListaSecCur_EstSec_EntAct.EntregaCalificacion, ListaSecCur_EstSec_EntAct.getEntregaId()};
                    miTablaNotas.addRow(datosFila);
                } else {
                    
                    Date mid = ListaSecCur_EstSec_EntAct.getActividadFechaEntrega();
                    DateFormat miFe = DateFormat.getDateInstance();
                    String miFec = miFe.format(mid);
                    
                    Date mid2 = ListaSecCur_EstSec_EntAct.getActividadFechaPublicacion();
                    DateFormat miFe2 = DateFormat.getDateInstance();
                    String miFec2 = miFe2.format(mid2);
                    
                    Object [] datosFila = {ListaSecCur_EstSec_EntAct.getTipoActividadDescripcion(), ListaSecCur_EstSec_EntAct.getActividadTitulo(), ListaSecCur_EstSec_EntAct.getActividadFechaEntrega(), ListaSecCur_EstSec_EntAct.getActividadFechaPublicacion(), ListaSecCur_EstSec_EntAct.getActividadPunteo(), ListaSecCur_EstSec_EntAct.getActividadFisica(), ListaSecCur_EstSec_EntAct.getActividadVirtual(), "No realizada", ListaSecCur_EstSec_EntAct.EntregaCalificacion, ListaSecCur_EstSec_EntAct.getEntregaId()};
                    miTablaNotas.addRow(datosFila);
                }
                
            }
        }
        Double nota = 0.0;
        if (TablaNotas.getRowCount() > 0) {
            for (int i = 0; i < TablaNotas.getRowCount(); i++) {
                nota = nota + Double.parseDouble(TablaNotas.getValueAt(i, 8).toString());
            }
        }
        this.jTextFieldNotaTotal.setText(nota + "");
        
    }

